package escape_sequences;

public class Concatenation {

        public static void main(String[] args) {

            System.out.println("Hello World"); // Will print hello world
            System.out.println("Hello " + "World"); // HelloWorld olarak yazar istedigimiz bu degil.
            System.out.println("Hello" + " World "); // istedigimiz bu sekilde
            System.out.println("Hello" + " " + "World");
            System.out.println(); //will add an empty line
            System.out.println("Today is Sunday");





        }
}
