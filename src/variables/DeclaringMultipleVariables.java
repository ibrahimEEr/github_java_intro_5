package variables;

public class DeclaringMultipleVariables {
    public static void main(String[] args) {

        int age1, age2, age3; //Declaring multiple variables at the same time. All has to be same datatype.


        double d1 = 25, d2 = 30, d3 = 40; //Declaring and assigning multiple variables at the same time.


        boolean b1 = true, b2, b3 = false; // Declaring 3 assigning 2 variables. Remember all has to be  same data types.

        int i3, i4;


        double c1; double c2;
        // double f1, double f2;   False because there must be a semicolon in between,
        //                               or second “double” needs to be removed.



    }
}
